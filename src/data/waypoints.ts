function findFix(fixIdentifier: string) {
  return getGraphData().filter(a => a.identifier == fixIdentifier)[0];
}

function findWaypoint(waypointIdentifier: string) {
  return getWaypoints().filter(a => a.identifier == waypointIdentifier)[0];
}

function findAirport(icao: string) {
  return getAirports().filter(a => a.identifier == icao)[0];
}

function getFixCoordinates(identifiers: string[]): any {
  let waypoints: any = [];
  identifiers.forEach(id => {
    waypoints.push(findFix(id));
  });

  return waypoints;
}

function getGraphData(): any {
  //@ts-ignore
  return getWaypoints().concat(getAirports());
}
